package graduate_training_CD;

public class Team {
	private String name;
	private int points;
	
	public Team(){
	}
	
	public String getTeamName(){
		return name;
	}
	
	public void setTeamName(String name){
		this.name = name;
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}
	
}
